import numpy as np

# np.vstack() 함수를 이용한 배열의 수직 스택
# np.vstack() 함수는 배열을 수직으로 쌓는 함수이다. 여기서는 ([1, 2, 3], [4, 5, 6])이라는 배열을 수직으로 쌓아서 [[1, 2, 3], [4, 5, 6]]이라는 배열이 만들어진다.
# np.vstack() 함수는 배열을 수직으로 쌓을 때, 첫 번째 배열이 두 번째 배열의 위에 오도록 쌓는다. 따라서, [1, 2, 3]이라는 배열이 먼저 나오고, 그 다음에 [4, 5, 6]이라는 배열이 나오도록 쌓인다.
a = np.vstack(([1, 2, 3], [4, 5, 6]))
print(a) # [[1 2 3] [4 5 6]]

# np.hstack() 함수를 이용한 배열의 수평 스택
# np.hstack() 함수는 배열을 수평으로 쌓는 함수이다. 여기서는 ([1, 2, 3], [4, 5, 6])이라는 배열을 수평으로 쌓아서 [1, 2, 3, 4, 5, 6]이라는 배열이 만들어진다.
# np.hstack() 함수는 배열을 수평으로 쌓을 때, 첫 번째 배열이 두 번째 배열의 왼쪽에 오도록 쌓는다. 따라서, [1, 2, 3]이라는 배열이 먼저 나오고, 그 다음에 [4, 5, 6]이라는 배열이 나오도록 쌓인다.
b = np.hstack(([1, 2, 3], [4, 5, 6]))
print(b) # [1 2 3 4 5 6]

# np.dstack() 함수를 이용한 배열의 깊이 스택
# np.dstack() 함수는 배열을 깊이로 쌓는 함수이다. 여기서는 ([1, 2, 3], [4, 5, 6])이라는 배열을 깊이로 쌓아서 [[[1, 4], [2, 5], [3, 6]]]이라는 배열이 만들어진다.
# np.dstack() 함수는 배열을 깊이로 쌓을 때, 첫 번째 배열이 두 번째 배열의 앞에 오도록 쌓는다. 따라서, [1, 2, 3]이라는 배열이 먼저 나오고, 그 다음에 [4, 5, 6]이라는 배열이 나오도록 쌓인다.
c = np.dstack(([1, 2, 3], [4, 5, 6]))
print(c) # [[[1 4] [2 5] [3 6]]]

# np.stack() 함수를 이용한 배열의 스택
# np.stack() 함수는 배열을 스택하는 함수이다. 여기서는 ([1, 2, 3], [4, 5, 6])이라는 배열을 스택해서 [[[1, 2, 3], [4, 5, 6]]]이라는 배열이 만들어진다.
# np.stack() 함수는 배열을 스택할 때, 첫 번째 배열이 두 번째 배열의 앞에 오도록 스택한다. 따라서, [1, 2, 3]이라는 배열이 먼저 나오고, 그 다음에 [4, 5, 6]이라는 배열이 나오도록 스택된다.
d = np.stack(([1, 2, 3], [4, 5, 6]))
print(d) # [[[1 2 3] [4 5 6]]]

#np.hsplit() 함수를 이용한 배열의 수평 분할
# np.hsplit() 함수는 배열을 수평으로 분할하는 함수이다. 여기서는 [1, 2, 3, 4, 5, 6]이라는 배열을 수평으로 분할해서 ([1, 2, 3], [4, 5, 6])이라는 배열이 만들어진다.
# np.hsplit() 함수는 배열을 수평으로 분할할 때, 첫 번째 배열이 두 번째 배열의 왼쪽에 오도록 분할한다. 따라서, [1, 2, 3]이라는 배열이 먼저 나오고, 그 다음에 [4, 5, 6]이라는 배열이 나오도록 분할된다.
e = np.hsplit([1, 2, 3, 4, 5, 6], 2)
print(e) # [array([1, 2, 3]), array([4, 5, 6])]

# np.vsplit() 함수를 이용한 배열의 수직 분할
# np.vsplit() 함수는 배열을 수직으로 분할하는 함수이다. 여기서는 [[1, 2, 3], [4, 5, 6]]이라는 배열을 수직으로 분할해서 ([[1, 2, 3]], [[4, 5, 6]])이라는 배열이 만들어진다.
# np.vsplit() 함수는 배열을 수직으로 분할할 때, 첫 번째 배열이 두 번째 배열의 위에 오도록 분할한다. 따라서, [[1, 2, 3]]이라는 배열이 먼저 나오고, 그 다음에 [[4, 5, 6]]이라는 배열이 나오도록 분할된다.
f = np.vsplit([[1, 2, 3], [4, 5, 6]], 2)
print(f) # [array([[1, 2, 3]]), array([[4, 5, 6]])]

# np.dsplit() 함수를 이용한 배열의 깊이 분할
# np.dsplit() 함수는 배열을 깊이로 분할하는 함수이다. 여기서는 [[[1, 4], [2, 5], [3, 6]]]이라는 배열을 깊이로 분할해서 ([[1], [2], [3]], [[4], [5], [6]])이라는 배열이 만들어진다.
# np.dsplit() 함수는 배열을 깊이로 분할할 때, 첫 번째 배열이 두 번째 배열의 앞에 오도록 분할한다. 따라서, [[1], [2], [3]]이라는 배열이 먼저 나오고, 그 다음에 [[4], [5], [6]]이라는 배열이 나오도록 분할된다.
g = np.dsplit([[[1, 4], [2, 5], [3, 6]]], 2)
print(g) # [array([[[1], [2], [3]]]), array([[[4], [5], [6]]])]

# 깊은 복사와 얕은 복사
# 깊은 복사와 얕은 복사는 배열을 복사하는 방법이다. 깊은 복사는 배열의 모든 요소를 새로운 메모리에 복사하는 것이고, 얕은 복사는 배열의 요소를 새로운 메모리에 복사하지 않고, 원래 배열의 요소를 참조하는 것이다.

# 깊은 복사 예시
h = np.array([1, 2, 3]) # 1차원 배열을 만들어준다. [1, 2, 3]은 리스트로 표현된 1차원 배열이다.
h_deep_copy = h.copy() # h의 깊은 복사를 만들어준다. copy() 함수는 배열의 깊은 복사를 만들어주는 함수이다. 따라서, h_deep_copy는 h의 깊은 복사인 배열이 된다.
h_deep_copy[0] = 10 # h_deep_copy의 첫 번째 요소를 10으로 변경한다. h_deep_copy는 h의 깊은 복사이므로, h_deep_copy의 요소를 변경해도 h의 요소는 변경되지 않는다. 따라서, h는 [1, 2, 3]이라는 배열이 되고, h_deep_copy는 [10, 2, 3]이라는 배열이 된다.
print(h) # [1 2 3] h는 [1, 2, 3]이라는 배열이 된다.
print(h_deep_copy) # [10  2  3] h_deep_copy는 [10, 2, 3]이라는 배열이 된다.

# 얕은 복사 예시
i = np.array([1, 2, 3]) # 1차원 배열을 만들어준다. [1, 2, 3]은 리스트로 표현된 1차원 배열이다.
i_shallow_copy = i.view() # i의 얕은 복사를 만들어준다. view() 함수는 배열의 얕은 복사를 만들어주는 함수이다. 따라서, i_shallow_copy는 i의 얕은 복사인 배열이 된다.
i_shallow_copy[0] = 10 # i_shallow_copy의 첫 번째 요소를 10으로 변경한다. i_shallow_copy는 i의 얕은 복사이므로, i_shallow_copy의 요소를 변경하면 i의 요소도 변경된다. 따라서, i는 [10, 2, 3]이라는 배열이 되고, i_shallow_copy는 [10, 2, 3]이라는 배열이 된다.
print(i) # [10  2  3] i는 [10, 2, 3]이라는 배열이 된다.
print(i_shallow_copy) # [10  2  3] i_shallow_copy는 [10, 2, 3]이라는 배열이 된다.
